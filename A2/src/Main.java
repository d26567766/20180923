import java.util.Scanner;
public class Main {

    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);//這是拿來用鍵盤輸入的語法
        int a, b;
        a = scn.nextInt();//這是處理
        b = scn.nextInt();

        System.out.println(a + b); //這是計算機輸出
        System.out.println(a - b);// +ln換行
        System.out.println(a * b);

    }

}
